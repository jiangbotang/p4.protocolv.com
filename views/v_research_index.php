          <p>The way chemotherapy and radiotherapy works is that they induce damage to our cells. Most of the cancer treatment
             drugs and radiation impose serious damage to our genetic material DNA. Cells of our body, cancer cells included, 
             have developed mechanisms to repair those damage, thus provide resistance to treatment. I study how cells repair the
            damage to find better drugs and treatment strategy.</p>
