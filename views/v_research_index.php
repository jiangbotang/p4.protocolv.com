        <h2>Research Expertise</h2>
        <p class="pad_bot2 marg_top"><strong>Chromatin structure, DNA repair, and PARP inhibitors </strong></p>
        <p class="pad_bot2">Radiation therapy, as well as PARP inhibitors, induces damage to cancer cells by breaking their DNA. However, cancer cells, like our normal cells, can manage to repair the breaks. The repair process happens in the 
         context of chromatin, in which DNA is packed and organized.<br>Chromatin structure, particularly, acetylation of histone tails 
         affects the repair process.</p>
